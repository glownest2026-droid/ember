/**
 * Generate catalogue cleanup SQL: deprecate bands + merge legacy category slugs.
 */
import fs from 'fs';
import path from 'path';
import XLSX from 'xlsx';
import { fileURLToPath } from 'url';
import {
  SOURCE_DIR,
  SOURCE_FILES,
  DEPRECATED_AGE_BAND_IDS,
  buildRenamePairs,
  q,
} from './spine-v2-slug-canonical.mjs';

const ROOT = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const BACKUP_DIR = path.join(ROOT, 'agent-tools/backups/spine-v2-workbooks-pre-slug-cleanup');

function loadSlugsFromWorkbooks(useBackup) {
  const slugs = new Set();
  for (const { file } of SOURCE_FILES) {
    const readPath = useBackup
      ? path.join(BACKUP_DIR, file)
      : path.join(SOURCE_DIR, file);
    const rows = XLSX.utils.sheet_to_json(XLSX.readFile(readPath).Sheets.discover_projection, {
      defval: '',
    });
    for (const r of rows) {
      const s = String(r.category_entity_id || '').trim();
      if (s) slugs.add(s);
    }
  }
  return slugs;
}

function buildDeprecateBandsSql() {
  const bandList = DEPRECATED_AGE_BAND_IDS.map((b) => q(b)).join(', ');
  return `-- Deprecate pre-Spine 2.0 pilot bands (history preserved, hidden from Discover).

UPDATE public.pl_age_bands ab
SET
  is_active = false,
  label = CASE
    WHEN ab.label LIKE '%[deprecated — pre-Spine 2.0]%' THEN ab.label
    ELSE ab.label || ' [deprecated — pre-Spine 2.0]'
  END,
  updated_at = now()
WHERE ab.id IN (${bandList});

UPDATE public.pl_age_band_development_need_category_types
SET is_active = false, updated_at = now()
WHERE age_band_id IN (${bandList});

UPDATE public.pl_age_band_ux_wrappers
SET is_active = false, updated_at = now()
WHERE age_band_id IN (${bandList});

UPDATE public.pl_age_band_category_type_products
SET is_active = false, updated_at = now()
WHERE age_band_id IN (${bandList});
`;
}

function buildMergePairSql({ legacy, canonical }) {
  return `
-- ${legacy} → ${canonical}
DO $$
BEGIN
  IF EXISTS (SELECT 1 FROM public.pl_category_types WHERE slug = ${q(legacy)})
     AND NOT EXISTS (SELECT 1 FROM public.pl_category_types WHERE slug = ${q(canonical)}) THEN
    UPDATE public.pl_category_types SET slug = ${q(canonical)}, updated_at = now() WHERE slug = ${q(legacy)};
  ELSIF EXISTS (SELECT 1 FROM public.pl_category_types WHERE slug = ${q(legacy)})
     AND EXISTS (SELECT 1 FROM public.pl_category_types WHERE slug = ${q(canonical)}) THEN
    UPDATE public.pl_age_band_development_need_category_types m
    SET category_type_id = canonical.id, updated_at = now()
    FROM public.pl_category_types legacy
    JOIN public.pl_category_types canonical ON canonical.slug = ${q(canonical)}
    WHERE legacy.slug = ${q(legacy)}
      AND m.category_type_id = legacy.id
      AND NOT EXISTS (
        SELECT 1
        FROM public.pl_age_band_development_need_category_types existing
        WHERE existing.age_band_id = m.age_band_id
          AND existing.development_need_id = m.development_need_id
          AND existing.category_type_id = canonical.id
      );

    UPDATE public.pl_age_band_development_need_category_types
    SET is_active = false, updated_at = now()
    WHERE category_type_id IN (SELECT id FROM public.pl_category_types WHERE slug = ${q(legacy)});

    UPDATE public.pl_category_type_images img
    SET category_type_id = canonical.id
    FROM public.pl_category_types legacy
    JOIN public.pl_category_types canonical ON canonical.slug = ${q(canonical)}
    WHERE legacy.slug = ${q(legacy)}
      AND img.category_type_id = legacy.id
      AND NOT EXISTS (
        SELECT 1
        FROM public.pl_category_type_images existing
        WHERE existing.category_type_id = canonical.id
          AND COALESCE(existing.age_band_id, '__global__') = COALESCE(img.age_band_id, '__global__')
          AND existing.is_active = true
      );

    UPDATE public.pl_category_type_images
    SET is_active = false
    WHERE category_type_id IN (SELECT id FROM public.pl_category_types WHERE slug = ${q(legacy)});

    UPDATE public.products p
    SET category_type_id = canonical.id, updated_at = now()
    FROM public.pl_category_types legacy
    JOIN public.pl_category_types canonical ON canonical.slug = ${q(canonical)}
    WHERE legacy.slug = ${q(legacy)}
      AND p.category_type_id = legacy.id;
  END IF;
END $$;
`;
}

const hasBackup = fs.existsSync(BACKUP_DIR);
const legacySlugs = loadSlugsFromWorkbooks(hasBackup);
const mergePairs = buildRenamePairs([...legacySlugs]);

const sql = `-- Spine 2.0 catalogue cleanup: deprecate out-of-scope bands + merge legacy category slugs
-- Generated by scripts/generate-catalogue-cleanup-sql.mjs
-- Idempotent: safe to re-run.

BEGIN;

${buildDeprecateBandsSql()}

${mergePairs.map(buildMergePairSql).join('\n')}

COMMIT;
`;

const migrationPath = path.join(
  ROOT,
  'supabase/migrations/20260628180000_catalogue_spine_v2_deprecate_merge.sql'
);
const mirrorPath = path.join(ROOT, 'supabase/sql/202606281800_catalogue_spine_v2_deprecate_merge.sql');
fs.mkdirSync(path.dirname(mirrorPath), { recursive: true });
fs.writeFileSync(migrationPath, sql);
fs.writeFileSync(mirrorPath, sql);

console.log(
  JSON.stringify(
    {
      deprecated_bands: DEPRECATED_AGE_BAND_IDS.length,
      merge_pairs: mergePairs.length,
      used_backup_for_legacy_slugs: hasBackup,
      migration: migrationPath,
    },
    null,
    2
  )
);
